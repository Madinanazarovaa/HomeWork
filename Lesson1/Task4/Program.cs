System.Console.WriteLine("Напишите число, чтобы программа показала все четные цифры от 1 до N");
int number = Convert.ToInt32(Console.ReadLine());

int evenNumber = 2;
{
    while
    (number >= evenNumber)
    {
        System.Console.Write(evenNumber + " ");
        evenNumber = evenNumber + 2;
    }
}